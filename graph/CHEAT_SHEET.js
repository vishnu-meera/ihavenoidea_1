// Quick reference for the iterative encoding pattern
function encodeTreeIterative(children, root) {
    const encoding = {}
    const inDegree = {}
    const parent = {}
    
    // 1. Find leaves, build parent map
    const leaves = []
    for(const node in children) {
        inDegree[node] = children[node].length
        if(inDegree[node] === 0) {
            leaves.push(node)
            encoding[node] = "()"  // Leaf encoding
        }
        for(const child of children[node]) {
            parent[child] = node
        }
    }
    
    // 2. Process bottom-up iteratively
    let queue = leaves
    while(queue.length > 0) {
        const nextQueue = []
        for(const node of queue) {
            const p = parent[node]
            if(p && !encoding[p]) {
                inDegree[p]--
                if(inDegree[p] === 0) {
                    // All children processed, encode parent
                    const childCodes = children[p]
                        .map(c => encoding[c])
                        .sort()  // ‚ö†Ô∏è MUST SORT!
                    encoding[p] = `(${childCodes.join('')})`
                    nextQueue.push(p)
                }
            }
        }
        queue = nextQueue
    }
    
    return encoding[root]
}
